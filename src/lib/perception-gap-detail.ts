import type { BigFiveDimension } from "./types";

export type GapDir3 = "self_higher" | "self_lower" | "match";

/** 自己評価 vs 友達の見方 (0-100) → 3 値方向。差 10pt 以内は match(ほぼ一致)。 */
export function gapDir3(selfPercent: number, otherPercent: number): GapDir3 {
  const d = selfPercent - otherPercent;
  if (Math.abs(d) <= 10) return "match";
  return d > 0 ? "self_higher" : "self_lower";
}

