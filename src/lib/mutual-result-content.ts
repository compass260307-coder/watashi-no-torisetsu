// =====================================================================
// レベル判定 (知覚スコア 0-10 → 高/中/低)
// =====================================================================
const LEVEL_HIGH = 6.5;
const LEVEL_LOW = 3.5;

export function levelForScore(score: number | undefined): TraitLevel {
  const v = typeof score === "number" ? score : 5;
  if (v >= LEVEL_HIGH) return "高";
  if (v < LEVEL_LOW) return "低";
  return "中";
}

/** DimensionGap (self%/other%) から向きを導く。等しい場合は self_higher 扱い。 */
export function directionOf(selfPercent: number, otherPercent: number): GapDirection {
  return selfPercent >= otherPercent ? "self_higher" : "self_lower";
}

