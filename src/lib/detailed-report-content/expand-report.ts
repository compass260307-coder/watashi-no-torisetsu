/**
 * 既存のタイプ固有本文を残しつつ、各章へ実践セクションを追加する。
 * じゅんすいユニコーンは手書きで同等以上に拡充済みのため対象外。
 */
export function expandDetailedReport(
  id: ThirtyTwoTypeId,
  report: DetailedReport,
): DetailedReport {
  if (id === "earnest-elephant__N") return report;

  const base = BASE_PROFILES[baseIdOf(id)];
  const axis = AXIS_PROFILES[nAxisOf(id)];
  const name = thirtyTwoName(id);
  const essence = thirtyTwoEssence(id);

  return {
    chapters: report.chapters.map((chapter, chapterIndex) => {
      const scenario = scenarioSection(chapterIndex, name, base, axis);
      return {
        ...chapter,
        sections: [
          ...chapter.sections,
          practicalSection(chapterIndex, name, essence, base, axis),
          ...(scenario ? [scenario] : []),
        ],
      };
    }),
  };
}
