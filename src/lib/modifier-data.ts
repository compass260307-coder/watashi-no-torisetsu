// ── ヘルパー関数 ──

/**
 * モディファイアラベルを取得 (例: "計画 × 繊細")
 */
export function getModifierLabel(c: CModifier, n: NModifier): string {
  return MODIFIER_LABELS[`${c}-${n}` as ModifierCode];
}

/**
 * タイプ × モディファイアからパラグラフを取得
 */
export function getModifierParagraph(
  typeId: TorisetsuTypeId,
  c: CModifier,
  n: NModifier,
): string {
  return MODIFIER_PARAGRAPHS[typeId][`${c}-${n}` as ModifierCode];
}

/**
 * 5 文字コード生成 (例: "EAO-C-N")
 * 注意: ベース 3 文字部分は Phase 2B の classifyType と整合させること
 */
export function generateFullCode(
  baseCode: string, // "EAO", "IMT" 等 (Phase 2B で生成)
  c: CModifier,
  n: NModifier,
): string {
  return `${baseCode}-${c}-${n}`;
}
