export function buildMoshimoScenes(
  scores: Partial<Record<BigFiveDimension, number>>,
  unlocked: boolean,
): MoshimoScene[] {
  const hl = (d: BigFiveDimension): "H" | "L" =>
    (typeof scores[d] === "number" ? (scores[d] as number) : 5) >= 5
      ? "H"
      : "L";
  return SCENES.map((scene) => {
    const gatedAndLocked = scene.gated && !unlocked;
    return {
      title: scene.title,
      chipLabel: scene.short ?? scene.title.replace(/(で)?のアナタ$/, ""),
      color: scene.color,
      body: gatedAndLocked
        ? ""
        : `${scene.main.prose[hl(scene.main.dim)]}${scene.spice.prose[hl(scene.spice.dim)]}`,
      locked: gatedAndLocked,
    };
  });
}
