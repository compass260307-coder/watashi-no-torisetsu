/** typeId から決定的なシード値 (Server Component で安定、型ごとにパターンが変わる) */
export function seedFromTypeId(typeId: string): number {
  let n = 0;
  for (const ch of typeId) n = (n + ch.charCodeAt(0)) % 997;
  return n;
}

/** 説明文から友達名の主語を除去 (主語省略で自然に読める形へ) */
function nameFreeDesc(body: string): string {
  return body
    .replace(/^\{B\}さん(?:に)?は、/, "")
    .replace(/\{B\}さん/g, "友達"); // 文中に残った場合の安全網
}

/**
 * 強み/あれっ? の 3 項目を「1 項目 = 1 段落」のセグメント列にする。
 * 段落 = 「ワード (pink)。説明 1 文 + 締め 1 文」。
 * items は PERCEIVED_BY_TYPE の先頭 3 つを渡す (残り 3 つは有料レポート用に温存)。
 */
export function weaveFound(
  items: RawItem[],
  kind: "strengths" | "surprises",
  seed: number,
  typeId?: SixteenTypeId,
): FoundParagraph[] {
  const closers = kind === "strengths" ? STRENGTH_CLOSERS : SURPRISE_CLOSERS;
  // 強みのみ: 型固有の具体描写を見え方と締めの間に挟んで 3 文化 (あれっ? は 2 文据え置き)
  const scenes =
    kind === "strengths" && typeId ? STRENGTH_SCENES[typeId] : undefined;
  return items.slice(0, 3).map((it, i) => {
    const word =
      kind === "surprises" ? (SOFT_WORD[it.title] ?? it.title) : it.title;
    // シードの偶奇 × 項目位置で締め文を選ぶ (3 段落で文末 3 種が揃う)
    const closer = closers[(seed + i * 2) % closers.length];
    const scene = scenes?.[i] ?? "";
    return [
      { text: word, pink: true },
      { text: `。${nameFreeDesc(it.body)}${scene}${closer}` },
    ];
  });
}
