-- =============================================================
-- 1. users.plan カラム追加
-- =============================================================
-- text 型、default 'free'。NOT NULL は付けない (NULL も 'free' 扱いを想定)。
-- 既存行は default 'free' で自動補完される。
alter table users
  add column if not exists plan text default 'free';

-- =============================================================
-- 2. インデックス (将来 plan = 'premium' で WHERE 集計が必要になる想定)
-- =============================================================
create index if not exists idx_users_plan on users(plan);


